package com.cwsoft.solid.ocp;
/**
 * The "O" in SOLID stands for Open/Closed Principle (OCP).
 * This principle states that software entities (classes, modules, functions, etc.) should be open for extension but
 * closed for modification.
 * <p>
 * Meaning of OCP:
 * Open for extension: You should be able to extend the behavior of a class or module without changing its existing
 * code.
 *
 * Closed for modification: Once a class or module is developed and tested, its source code should not be modified
 * to add new functionality. Instead, new functionality should be added through extension mechanisms,
 * like inheritance or composition.
 *
 * Why is OCP important?
 * The Open/Closed Principle helps reduce the risk of breaking existing code when new functionality is added,
 * improving maintainability and scalability. It encourages developers to write modular and extensible code,
 * promoting code reuse.
 */